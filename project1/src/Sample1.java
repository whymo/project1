// 실행클라스라고 부른다(main 메소드가 있음)
// 라이브러리 클래스라고 부른다(main 메소드가 없는 클래스)
public class Sample1 {
	
	// JVM이 실행시킨다! -> main 메소드를 호출(invoke)한다!
	// main : 메소드(method) -> 어떤 문제를 해결하는 방법, "실행진입점" (Entry Point)
	public static void main(String[] args) {
		System.out.println("Hello, world");
		
	} // main : entry point 역할을 한다. 특수한 메소드이다!
	
} // end class 끝을 알리는 주석은 꼭 넣기
