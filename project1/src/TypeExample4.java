
public class TypeExample4 {
	
	public static void main(String[] args) {
		
		long var1 = 10; 
		long var2 = 20L;
		
		//long var3 = 10000000000; // L이 없으면 int라고 가정한다
		
		long var4 = 10000000000L;
		// int로 보관하기 힘듬 숫자가 커서
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var4);
	} // main

} //end class
