
public class TypeExample5 {
	
	public static void main(String[] args) {
		
		// 참과 거짓 논리타입 : boolean -> 운영체제마다 다르지만 (1비트만 사용한다) 
		boolean stop = false;
		
		if(stop) { 
			System.out.println("중지합니다."); //참이면 이거 실행
		} else {
			System.out.println("시작합니다."); //참이 아니면 이걸 실행
		} // if-else, 조건문 , 제어문
		
		
	} // main

} //endclass
